<h5><span class="text-info">So funktioniert das</span></h5>
<p class="card-text">
    Du kannst Nachrichten an Gruppen von Vereins- oder Rundenmitarbeitenden oder aber auch Benutzergruppen verschicken.
    Die Nachrichten werden zu einem von Dir angegebenen Datum automatisch als eMail versendet. Benutzergruppen sehen die Nachricht auf der Startseite.
</p>
<h5><span class="text-info">Eine neue Nachricht schreiben.</span></h5>
<p class="card-text">
    Die Seite zum Erstellen einer neuen Nachricht erreichst du über das Menü: <b><i>Administration -> Nachrichten -> Neue Nachricht</i></b>

    Hier kannst Du Begrüßung, Nachrichtentext, Grußformel sowie die Adressaten erfassen:
    <img class="rounded x-auto d-block border border-dark img-fluid" src="{{ asset('img/message_new.png') }}" alt="Neue Nachricht">
</p>
<p class="card-text">
    In den Feldern "Senden an", "mit Kopie an" und "an Anwendende" ist eine mehrfach Auswahl von Gruppen aber auch eine Abwahl aller Gruppen möglich.
</p>
<p class="card-text">
    Die Gruppen in "Senden an" und "mit Kopie an" erhalten eMails als Addressat oder in Kopie. Dazu werden die bei den Vereins- bzw. Rundenmitarbeitenden hinterlegten eMail -Adressen benutzt.
</p>
<p class="card-text">
    Die Gruppen in "an Anwendende" sehen die NAchricht nach einm Login im Hauptfenster der Anwendung unter "Nachrichten".
</p>
