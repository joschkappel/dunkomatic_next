<h5><span class="text-info">So funktioniert das</span></h5>
<p class="card-text">
    Es gibt folgende Rollen/Zugriffsrechte für Benutzer:innen :
    <ul>
        <li><spawn class="text-danger font-weight-bold">Kandidat</spawn> - wird vom System zugeordnet solange ein Konto nocht nicht freigegeben ist. Kandidaten haben KEINEN Zugang zur Anwendung</li>
        <li><spawn class="text-danger font-weight-bold">Gast</spawn> - ist die Standard Rolle und gilt für jedes Konto, das keine anderen Rechte besitzt. Gäst könne KEINE Daten ändern</li>
        <li><spawn class="text-danger font-weight-bold">Vereinsverwaltung</spawn> - wird von der Bezirksleitung zugeordnet. Vereinsverwaltungsrechte erlauben die Daten eines oder mehrerer Verein zu ändern (inklusive Mannschaftsmeldung, Ziffernwahl etc)</li>
        <li><spawn class="text-danger font-weight-bold">Rundenverwaltung</spawn> -  wird von der Bezirksleitung zugeordnet. Rundenverwaltungsrechte erlauben die Daten einer oder mehrerer Spielrunden zu ändern</li>
    </ul>
</p>
